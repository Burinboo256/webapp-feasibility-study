/*
  Cohort feasibility study app
  Initial SQL Server setup script

  Purpose:
  1. Create the clinical tables expected by the current SQL builder and SQL Server repository.
  2. Create the application tables needed for login, sessions, OTP flows, saved cohorts, and feasibility audit logging.
  3. Provide a practical starter schema for adopters who want to stand up the project on a fresh database.

  Notes:
  - This is a guide schema for initial setup, not a copy of the upstream hospital warehouse.
  - If your source system uses different table or column names, either adapt this script or update the app mappings.
  - The clinical table names below intentionally match the current generated SQL:
      Patient_Info, Diagnosis, Laboratory, Medication
*/

SET ANSI_NULLS ON;
SET QUOTED_IDENTIFIER ON;
GO

/* ============================================================================
   Core clinical tables used by the current SQL builder
   ============================================================================ */

IF OBJECT_ID(N'dbo.Patient_Info', N'U') IS NULL
BEGIN
  CREATE TABLE dbo.Patient_Info (
    OH_PID VARCHAR(20) NOT NULL,
    BIRTH_DATE DATE NULL,
    SEX VARCHAR(20) NULL,
    SEX_CODE CHAR(1) NULL,
    PATIENT_TYPE_CODE VARCHAR(20) NULL,
    PATIENT_TYPE_NAME VARCHAR(100) NULL,
    RACE_NAME VARCHAR(50) NULL,
    CREATED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_Patient_Info_CREATED_AT DEFAULT SYSUTCDATETIME(),
    UPDATED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_Patient_Info_UPDATED_AT DEFAULT SYSUTCDATETIME(),
    CONSTRAINT PK_Patient_Info PRIMARY KEY CLUSTERED (OH_PID)
  );
END;
GO

IF OBJECT_ID(N'dbo.Diagnosis', N'U') IS NULL
BEGIN
  CREATE TABLE dbo.Diagnosis (
    DIAGNOSIS_ID BIGINT IDENTITY(1,1) NOT NULL,
    OH_PID VARCHAR(20) NOT NULL,
    ICD_CODE VARCHAR(20) NOT NULL,
    VISIT_DATE DATE NOT NULL,
    DISEASE_NAME VARCHAR(200) NULL,
    VISIT_OR_ADMIT_NO VARCHAR(30) NULL,
    PATIENT_CATEGORY VARCHAR(10) NULL,
    ICD_VERSION VARCHAR(10) NULL,
    DIAGNOSIS_TYPE CHAR(1) NULL,
    AGE_AT_VISIT SMALLINT NULL,
    DIAGNOSIS_SEQ SMALLINT NULL,
    DIAGNOSIS_GRP VARCHAR(10) NULL,
    CREATED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_Diagnosis_CREATED_AT DEFAULT SYSUTCDATETIME(),
    CONSTRAINT PK_Diagnosis PRIMARY KEY CLUSTERED (DIAGNOSIS_ID),
    CONSTRAINT FK_Diagnosis_Patient_Info FOREIGN KEY (OH_PID) REFERENCES dbo.Patient_Info (OH_PID)
  );
END;
GO


    CONSTRAINT FK_Feasibility_Run_Logs_App_Users FOREIGN KEY (USER_ID) REFERENCES dbo.App_Users (USER_ID),
    CONSTRAINT CK_Feasibility_Run_Log_ATTRITION CHECK (ATTRITION IS NULL OR ISJSON(ATTRITION) = 1),
    CONSTRAINT CK_Feasibility_Run_Log_SELECTED_CONCEPTS CHECK (SELECTED_CONCEPTS IS NULL OR ISJSON(SELECTED_CONCEPTS) = 1),
    CONSTRAINT CK_Feasibility_Run_Log_CONFIG CHECK (ISJSON(CONFIG) = 1)
  );
END;
GO


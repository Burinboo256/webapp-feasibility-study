IF OBJECT_ID(N'dbo.Feasibility_Run_Logs', N'U') IS NULL
BEGIN
  CREATE TABLE dbo.Feasibility_Run_Logs (
    RUN_ID UNIQUEIDENTIFIER NOT NULL CONSTRAINT DF_Feasibility_Run_Log_RUN_ID DEFAULT NEWID(),
    SESSION_ID NVARCHAR(128) NULL,
    USER_ID UNIQUEIDENTIFIER NULL,
    CREATED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_Feasibility_Run_Log_CREATED_AT DEFAULT SYSUTCDATETIME(),
    QUESTION NVARCHAR(1000) NULL,
    INDEX_ELIGIBLE_COUNT INT NOT NULL CONSTRAINT DF_Feasibility_Run_Log_INDEX_ELIGIBLE_COUNT DEFAULT 0,
    FINAL_COUNT INT NOT NULL CONSTRAINT DF_Feasibility_Run_Log_FINAL_COUNT DEFAULT 0,
    EXCLUDED_COUNT INT NOT NULL CONSTRAINT DF_Feasibility_Run_Log_EXCLUDED_COUNT DEFAULT 0,
    ATTRITION NVARCHAR(MAX) NULL,

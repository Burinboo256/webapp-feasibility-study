/* Helpful indexes for cohort feasibility queries */

IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = N'IX_Diagnosis_OH_PID_ICD_CODE_VISIT_DATE' AND object_id = OBJECT_ID(N'dbo.Diagnosis'))
BEGIN
  CREATE INDEX IX_Diagnosis_OH_PID_ICD_CODE_VISIT_DATE
    ON dbo.Diagnosis (OH_PID, ICD_CODE, VISIT_DATE);
END;
GO

IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = N'IX_Laboratory_OH_PID_LAB_CODE_RESULT_DATE' AND object_id = OBJECT_ID(N'dbo.Laboratory'))
BEGIN
  CREATE INDEX IX_Laboratory_OH_PID_LAB_CODE_RESULT_DATE
    ON dbo.Laboratory (OH_PID, LAB_CODE, RESULT_DATE)
    INCLUDE (LAB_VALUE, LAB_NAME, LAB_GROUP_NAME);
END;
GO

IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = N'IX_Medication_OH_PID_DRUG_CODE_ORDER_DATE' AND object_id = OBJECT_ID(N'dbo.Medication'))
BEGIN
  CREATE INDEX IX_Medication_OH_PID_DRUG_CODE_ORDER_DATE
    ON dbo.Medication (OH_PID, DRUG_CODE, ORDER_DATE)
    INCLUDE (DRUG_NAME, DRUG_GROUP_NAME);
END;
GO

/* ============================================================================
   Application tables for auth, state, saved cohorts, and audit
   ============================================================================ */

IF OBJECT_ID(N'dbo.App_Users', N'U') IS NULL
BEGIN
  CREATE TABLE dbo.App_Users (
    USER_ID UNIQUEIDENTIFIER NOT NULL CONSTRAINT DF_App_User_USER_ID DEFAULT NEWID(),
    EMAIL NVARCHAR(255) NOT NULL,
    PASSWORD_HASH NVARCHAR(255) NULL,
    NAME NVARCHAR(255) NOT NULL,
    PROVIDER NVARCHAR(50) NOT NULL CONSTRAINT DF_App_User_AUTH_PROVIDER DEFAULT N'credentials',
    ROLE NVARCHAR(50) NOT NULL CONSTRAINT DF_App_User_ROLE DEFAULT N'researcher',
    GOOGLE_SUB NVARCHAR(255) NULL,
    ACTIVE BIT NOT NULL CONSTRAINT DF_App_User_ACTIVE DEFAULT 1,
    CREATED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_App_User_CREATED_AT DEFAULT SYSUTCDATETIME(),
    PASSWORD_UPDATED_AT DATETIME2(0) NULL,
    LAST_LOGIN_AT DATETIME2(0) NULL,
    CONSTRAINT PK_App_Users PRIMARY KEY CLUSTERED (USER_ID),
    CONSTRAINT UQ_App_Users_EMAIL UNIQUE (EMAIL)
  );
END;
GO

IF OBJECT_ID(N'dbo.User_Sessions', N'U') IS NULL
BEGIN
  CREATE TABLE dbo.User_Sessions (
    SESSION_ID NVARCHAR(128) NOT NULL,
    USER_ID UNIQUEIDENTIFIER NOT NULL,
    STARTED_AT DATETIME2(0) NOT NULL CONSTRAINT DF_User_Sessions_STARTED_AT DEFAULT SYSUTCDATETIME(),
    LAST_SEEN_AT DATETIME2(0) NOT NULL CONSTRAINT DF_App_Session_LAST_SEEN_AT DEFAULT SYSUTCDATETIME(),
    PAGE_VIEWS INT NOT NULL CONSTRAINT DF_User_Sessions_PAGE_VIEWS DEFAULT 0,
    RUN_COUNT INT NOT NULL CONSTRAINT DF_User_Sessions_RUN_COUNT DEFAULT 0,
    USER_AGENT NVARCHAR(512) NULL,
    EXPIRES_AT DATETIME2(0) NOT NULL,
    REVOKED_AT DATETIME2(0) NULL,
    IP_ADDRESS NVARCHAR(64) NULL,
    CONSTRAINT PK_User_Sessions PRIMARY KEY CLUSTERED (SESSION_ID),
    CONSTRAINT FK_User_Sessions_App_Users FOREIGN KEY (USER_ID) REFERENCES dbo.App_Users (USER_ID)
  );
END;
GO

